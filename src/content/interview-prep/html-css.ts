const topicGroupDescriptions: Record<string, string> = {
  HTML: 'Core document structure, semantics, metadata, and practical browser-facing markup decisions.',
  CSS: 'Cascade, specificity, selector behavior, and maintainable styling patterns.',
  'Responsive Design': 'Layout decisions that survive small screens, dynamic content, and real breakpoints.',
  Accessibility: 'Keyboard, labels, semantics, ARIA, and production-safe inclusive UI behavior.',
  'Real Project Scenarios': 'Practical UI bug patterns, SEO trade-offs, and release-time frontend issues.',
};

function buildTopicGroups(): InterviewPrepTopicGroup[] {
  return htmlCssInterviewPrepTopicGroups.map((group) => ({
    id: group.id,
    title: group.title,
    description: topicGroupDescriptions[group.title] ?? 'Focused interview preparation topics.',
    topics: group.topics.map((topic) => topic.title),
  }));
}

function roleAnswer(base: string, level: ExperienceLevelId) {
  switch (level) {
    case 'beginner':
      return `${base} Start with the core definition and one simple example.`;
    case 'mid':
      return `${base} Add one implementation detail and one common mistake.`;
    case 'senior':
      return `${base} Connect it to accessibility, responsive behavior, or maintainability.`;
    case 'architect':
      return `${base} Frame it as a reusable frontend standard, not only a one-page fix.`;
  }
}

