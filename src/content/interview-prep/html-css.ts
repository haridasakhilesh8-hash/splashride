function buildTopicMetadata(topicGroups: InterviewPrepTopicGroup[], items: InterviewPrepQuestion[]): InterviewPrepTopicMetadata[] {
  return topicGroups.flatMap((group) =>
    group.topics.map((topicTitle) => {
      const category = htmlCssInterviewPrepTopicGroups.flatMap((groupItem) => groupItem.topics).find((topic) => topic.title === topicTitle)?.category ?? topicTitle;
      const categoryQuestions = items.filter((item) => item.category === category);
      const difficultyCounts = categoryQuestions.reduce<Record<'Beginner' | 'Intermediate' | 'Advanced' | 'Architect', number>>(
        (counts, item) => {
          counts[item.difficultyLevel] += 1;
          return counts;
        },
        { Beginner: 0, Intermediate: 0, Advanced: 0, Architect: 0 },
      );

      return {
        category,
        topicGroup: group.title,
        totalQuestions: categoryQuestions.length,
        estimatedPreparationMinutes: Math.max(10, categoryQuestions.length * 6),
        questionsPerPage: 6,
        totalPages: Math.max(1, Math.ceil(categoryQuestions.length / 6)),
        difficultyCounts,
      };
    }),
  );
}

function buildTopicPreparationSets(items: InterviewPrepQuestion[]): InterviewPrepTopicPreparationSet[] {
  const categories = Array.from(new Set(items.map((item) => item.category)));
  return categories.map((category) => {
    const ids = items.filter((item) => item.category === category).sort((a, b) => (a.mostAskedRank ?? 999) - (b.mostAskedRank ?? 999)).map((item) => item.id);
    return {
      category,
      mostAskedQuestionIds: ids.slice(0, 3),
      top5QuestionIds: ids.slice(0, 5),
      top10QuestionIds: ids.slice(0, 10),
      thirtyMinuteQuestionIds: ids.slice(0, Math.min(5, ids.length)),
      sixtyMinuteQuestionIds: ids,
      lastMinuteQuestionIds: ids.slice(0, Math.min(3, ids.length)),
    };
  });
}

