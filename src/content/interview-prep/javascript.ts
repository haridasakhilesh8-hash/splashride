function buildTopicGroups(): InterviewPrepTopicGroup[] {
  return javascriptInterviewPrepTopicGroups.map((group) => ({
    id: group.id,
    title: group.title,
    description: topicGroupDescriptions[group.title] ?? 'Focused JavaScript preparation topics.',
    topics: group.topics.map((topic) => topic.title),
  }));
}

function roleAnswer(base: string, level: ExperienceLevelId) {
  switch (level) {
    case 'beginner':
      return `${base} Keep the example simple and browser-focused.`;
    case 'mid':
      return `${base} Add one debugging or implementation detail.`;
    case 'senior':
      return `${base} Connect it to maintainability, async behavior, or real UI bugs.`;
    case 'architect':
      return `${base} Frame it as a platform or system-level frontend standard.`;
  }
}

