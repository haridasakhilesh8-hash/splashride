function normalizeCategory(category: string): string {
  return pythonCategoryMap[category] ?? category;
}

function question(spec: QuestionSpec): InterviewPrepQuestion {
  const normalizedCategory = normalizeCategory(spec.category);
  return {
    ...spec,
    category: normalizedCategory,
    topicGroup: normalizedCategory,
    technologyId: 'python',
    roleAnswers: {
      junior: roleAnswer(spec.shortAnswer, 'beginner'),
      mid: roleAnswer(spec.shortAnswer, 'mid'),
      senior: roleAnswer(spec.shortAnswer, 'senior'),
      architect: roleAnswer(spec.shortAnswer, 'architect'),
    },
  };
}

function buildCoverageQuestions(category: string, seeds: CoverageSeed[]): InterviewPrepQuestion[] {
  return seeds.flatMap((seed, index) => {
    const difficultyLevel = seed.difficultyLevel ?? 'Intermediate';
    const experienceLevel = seed.experienceLevel ?? 'mid';
    const questionType = seed.questionType ?? 'Practical';
    const frequencyScore = seed.frequencyScore ?? Math.max(66, 91 - index);
    const lowerLabel = seed.label.toLowerCase();

    return [
      question({
        id: `${seed.id}-concept`,
        category,
        topicGroup: category,
        questionType,
        question: `What should you know about ${seed.label} in ${category}?`,
        shortAnswer: seed.answer,
        detailedAnswer: [
          seed.answer,
          `In real Python work, ${seed.projectUse}.`,
          `A strong answer should connect ${lowerLabel} to maintainability, debugging, or production reliability instead of stopping at syntax.`,
        ],
        realProjectExample: `A team used ${lowerLabel} when ${seed.projectUse}.`,
        productionScenario: `A production issue appears because ${seed.productionRisk}.`,
        commonMistakes: [
          `Treating ${lowerLabel} as trivia instead of explaining how it affects real scripts or services.`,
          `Giving a definition without showing where ${lowerLabel} matters in actual code.`,
        ],
        followUpQuestions: [
          `When is ${seed.label} the better choice?`,
          `What is a common debugging step related to ${seed.label}?`,
        ],
        interviewerExpectation: `The interviewer wants to know whether you can explain ${lowerLabel} clearly and connect it to practical Python delivery work.`,
        frequencyScore,
        commonWrongAnswer: `A weak answer treats ${lowerLabel} as theory only and ignores how it behaves in real projects.`,
        architectPerspective: `At scale, teams should standardize how they use ${lowerLabel} so Python code stays predictable, testable, and easier to operate.`,
        keyTakeaway: `${seed.label} matters because it changes how Python code behaves, scales, or fails in real systems.`,
        difficultyLevel,
        experienceLevel,
        relatedTopics: seed.relatedTopics,
      }),
      question({
        id: `${seed.id}-scenario`,
        category,
        topicGroup: category,
        questionType: 'Scenario',
        question: `What production mistake is common with ${seed.label} in ${category}?`,
        shortAnswer: `A common issue with ${lowerLabel} is that ${seed.productionRisk}.`,
        detailedAnswer: [
          `A common issue with ${lowerLabel} is that ${seed.productionRisk}.`,
          `You should explain the root cause, how you would debug it, and which safer pattern you would use next.`,
          `Interviewers usually care about your troubleshooting order and practical judgment, not only the final fix.`,
        ],
        realProjectExample: `A release needed follow-up work because ${seed.projectUse}.`,
        productionScenario: `The script or service misbehaves because ${seed.productionRisk}.`,
        commonMistakes: [
          `Patching the symptom without fixing the underlying ${lowerLabel} decision.`,
          `Assuming the issue only affects local scripts and not production services or automation jobs.`,
        ],
        followUpQuestions: [
          `How would you verify the fix safely?`,
          `What reusable guardrail would prevent this issue next time?`,
        ],
        interviewerExpectation: `The interviewer wants to hear a practical debugging answer, not only a conceptual definition of ${lowerLabel}.`,
        frequencyScore: Math.max(60, frequencyScore - 4),
        commonWrongAnswer: `A weak answer says "just change the code" without explaining the runtime failure pattern or validation steps.`,
        architectPerspective: `Repeated ${lowerLabel} failures should become shared libraries, coding standards, tests, or operational checks instead of recurring one-off fixes.`,
        keyTakeaway: `You should be able to explain both the concept behind ${seed.label} and the production bug pattern around it.`,
        difficultyLevel,
        experienceLevel,
        relatedTopics: seed.relatedTopics,
      }),
    ];
  });
}

function mergeQuestions(...groups: InterviewPrepQuestion[][]): InterviewPrepQuestion[] {
  const seen = new Set<string>();
  return groups.flat().filter((item) => {
    if (seen.has(item.id)) {
      return false;
    }
    seen.add(item.id);
    return true;
  });
}

