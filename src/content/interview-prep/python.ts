function buildTopicGroups(): InterviewPrepTopicGroup[] {
  return pythonInterviewPrepTopicGroups.map((group) => ({
    id: group.id,
    title: group.title,
    description: topicGroupDescriptions[group.title] ?? 'Focused Python interview preparation topics.',
    topics: group.topics.map((topic) => topic.title),
  }));
}

function roleAnswer(base: string, level: ExperienceLevelId) {
  switch (level) {
    case 'beginner':
      return `${base} Keep the explanation concrete and example-driven.`;
    case 'mid':
      return `${base} Add one debugging or implementation detail from real code.`;
    case 'senior':
      return `${base} Connect it to maintainability, performance, or production support.`;
    case 'architect':
      return `${base} Frame it in terms of team standards, scaling, or reliability trade-offs.`;
  }
}

